		<div id="mw-content-text" lang="en" dir="ltr" class="mw-content-ltr"><div class="mw-parser-output"><p>SubPos Nodes or existing Wi-Fi access points are used to transmit encoded information in a standard Wi-Fi beacon frame which is then used for position trilateration. The Nodes, while not necessary part of the SubPos standard, provide a pre-calibrated device that is easy to configure and can be dotted about with ease.
</p>
<h2><span class="mw-headline" id="Features:">Features:</span></h2>
<ul><li>Easy to use config menu.</li>
<li>USB power connector.</li>
<li>USB to UART.</li>
<li>USB firmware updates.</li>
<li>Frequency shifting.</li>
<li>Secure (generates raw beacon frames).</li>
<li>Open source.</li></ul>
