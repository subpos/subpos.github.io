<h2><span id="Accelerometer/Sensor_Information_and_Profiled_Path"></span><span class="mw-headline" id="Accelerometer.2FSensor_Information_and_Profiled_Path">Accelerometer/Sensor Information and Profiled Path</span></h2>
<pre>   Good for metro only.
   Low accuracy.
   May require data connectivity.
</pre>
<p>Example:
</p><p><a rel="nofollow" class="external free" href="http://grouplens.org/site-content/uploads/bhecht_sigspatial2014_subwayps.pdf">http://grouplens.org/site-content/uploads/bhecht_sigspatial2014_subwayps.pdf</a>
</p>
<h2><span class="mw-headline" id="GPS_Relay">GPS Relay</span></h2>
<pre>   Can be very expensive to set up.
   Very accurate (depending on setup). 
</pre>
<p>Example:
</p><p><a rel="nofollow" class="external free" href="http://www.karstworlds.com/2011/01/underground-gps-finally-possible.html">http://www.karstworlds.com/2011/01/underground-gps-finally-possible.html</a>
</p>
<h2><span class="mw-headline" id="Proprietary">Proprietary</span></h2>
<pre>   Can be expensive.
   Accuracy depends on implementation. 
</pre>
<p>Example:
</p><p><a rel="nofollow" class="external free" href="http://www.businesswire.com/news/home/20130819006343/en/Minetec-Partners-CSIRO-Deliver-Revolutionary-Underground-Positioning#.VQ7Sly5WJ-8">http://www.businesswire.com/news/home/20130819006343/en/Minetec-Partners-CSIRO-Deliver-Revolutionary-Underground-Positioning#.VQ7Sly5WJ-8</a>
</p><p><a rel="nofollow" class="external free" href="http://www.darpa.mil/Our_Work/STO/Programs/Adaptable_Navigation_Systems_(ANS).aspx">http://www.darpa.mil/Our_Work/STO/Programs/Adaptable_Navigation_Systems_%28ANS%29.aspx</a>
</p><p><a rel="nofollow" class="external free" href="http://www.essensium.com/technology.html">http://www.essensium.com/technology.html</a>
</p>
<h2><span class="mw-headline" id="Wi-Fi_Positioning_Systems">Wi-Fi Positioning Systems</span></h2>
<pre>   Requires profiling of area (profiling can be inaccurate depending on factors such as time of day, changes over time).
   Requires data connectivity and database server.
   Medium accuracy.
   Widely used.
   Doesn't work where you cannot profile the access points against known positioning (GPS).
</pre>
<p>Example:
</p><p><a rel="nofollow" class="external free" href="https://support.google.com/gmm/answer/1646140?hl=en">https://support.google.com/gmm/answer/1646140?hl=en</a>
<a rel="nofollow" class="external free" href="http://lens1.csie.ncku.edu.tw/Library/Paper/IEEE MDM 2009/data/3650a371.pdf">http://lens1.csie.ncku.edu.tw/Library/Paper/IEEE%20MDM%202009/data/3650a371.pdf</a>
</p><p><a rel="nofollow" class="external free" href="http://www.smh.com.au/digital-life/smartphone-apps/indoor-gps-every-step-you-take-every-move-you-make-googles-got-maps-for-you-20121115-29e1b.html">http://www.smh.com.au/digital-life/smartphone-apps/indoor-gps-every-step-you-take-every-move-you-make-googles-got-maps-for-you-20121115-29e1b.html</a>
</p><p><a rel="nofollow" class="external free" href="http://redpin.org/">http://redpin.org/</a>
</p>
<h2><span class="mw-headline" id="More_Technologies">More Technologies</span></h2>
