</p><p>iBeacon - <a rel="nofollow" class="external free" href="http://en.wikipedia.org/wiki/IBeacon">http://en.wikipedia.org/wiki/IBeacon</a>, uses bluetooth. Lower range, <a rel="nofollow" class="external text" href="https://www.youtube.com/watch?v=dMWEl6GBGqk">can be used to calculate position</a>, requires database connectivity, designed to be context aware rather than used for positioning.
