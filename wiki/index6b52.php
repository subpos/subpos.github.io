		<div id="mw-content-text" lang="en" dir="ltr" class="mw-content-ltr"><div class="mw-parser-output"><p>This position augmentation demonstration is for the Billion BiPAC 7800VDPX. If you have another router that is known to work with a similar method, please help the site by documenting the process <a href="http://subpos.org/wiki/index.php/Access_Point_Support" title="Access Point Support">here</a>.
</p><p>This router allows you to set additional SSIDs for a particular Wi-Fi adapter (2.4G) via this page <a rel="nofollow" class="external free" href="http://192.168.1.254/wlswitchinterface0.wl">http://192.168.1.254/wlswitchinterface0.wl</a>
</p><p>You should be able to convert the position coder's hex character output to ASCII characters and copy and paste this into the SSID text field. If you have problems with this, then lets look at the post command for the form:
</p>
<pre><a rel="nofollow" class="external free" href="http://192.168.1.254/wlcfg.wl">http://192.168.1.254/wlcfg.wl</a>?
wlSsidIdx=0&amp;wlEnableHspot=0&amp;wlEnbl=1&amp;wlHide=0&amp;wlAPIsolation=0&amp;wlSsid=subpos
&amp;wlCountry=AU&amp;wlMaxAssoc=16&amp;wlDisableWme=0&amp;wlEnableWmf=0&amp;wlSsid_wl0v1=wl0_Guest1&amp;wlHide_wl0v1=0
&amp;wlAPIsolation_wl0v1=0&amp;wlDisableWme_wl0v1=0&amp;wlEnableWmf_wl0v1=0&amp;wlMaxAssoc_wl0v1=16&amp;wlEnbl_wl0v1=1
&amp;wlSsid_wl0v2=wl0_Guest2&amp;wlHide_wl0v2=0&amp;wlAPIsolation_wl0v2=0&amp;wlDisableWme_wl0v2=0&amp;wlEnableWmf_wl0v2=0
&amp;wlMaxAssoc_wl0v2=16&amp;wlEnbl_wl0v2=0&amp;wlSsid_wl0v3=wl0_Guest3&amp;wlHide_wl0v3=0&amp;wlAPIsolation_wl0v3=0
&amp;wlDisableWme_wl0v3=0&amp;wlEnableWmf_wl0v3=0&amp;wlMaxAssoc_wl0v3=16&amp;wlEnbl_wl0v3=0&amp;wlSyncNvram=1
&amp;sessionKey=44797442
</pre>
<p>If we modify the form data object "wlSsid_wl0v1" (this label corresponds with the "name" value of the textbox in the html code) with the following URL coded hex data (note that the Billion terminates its SSID string with the 0x00 null character and also doesn't like the space character, so the coder needs to be adjusted to mask these characters for this router), we get the following post command:
</p>
<pre><a rel="nofollow" class="external free" href="http://192.168.1.254/wlcfg.wl">http://192.168.1.254/wlcfg.wl</a>?
wlSsidIdx=0&amp;wlEnableHspot=0&amp;wlEnbl=1&amp;wlHide=0&amp;wlAPIsolation=0&amp;wlSsid=subpos
&amp;wlCountry=AU&amp;wlMaxAssoc=16&amp;wlDisableWme=0&amp;wlEnableWmf=0&amp;wlSsid_wl0v1=
<b>%53%50%53%01%01%63%3b%15%0b%07%3b%76%7f%31%07%5b%4d%15%1c%30%07%01%01%21%01%11%30%62%01%07%44</b>
&amp;wlHide_wl0v1=0&amp;wlAPIsolation_wl0v1=0&amp;wlDisableWme_wl0v1=0&amp;wlEnableWmf_wl0v1=0
&amp;wlMaxAssoc_wl0v1=0&amp;wlEnbl_wl0v1=1&amp;wlSsid_wl0v2=wl0_Guest2&amp;wlHide_wl0v2=0
&amp;wlAPIsolation_wl0v2=0&amp;wlDisableWme_wl0v2=0&amp;wlEnableWmf_wl0v2=0&amp;wlMaxAssoc_wl0v2=16
&amp;wlEnbl_wl0v2=0&amp;wlSsid_wl0v3=wl0_Guest3&amp;wlHide_wl0v3=0&amp;wlAPIsolation_wl0v3=0
&amp;wlDisableWme_wl0v3=0&amp;wlEnableWmf_wl0v3=0&amp;wlMaxAssoc_wl0v3=16&amp;wlEnbl_wl0v3=0
&amp;wlSyncNvram=1&amp;sessionKey=44797442
</pre>
<p>Pop that into your web browser address bar and submit it. If it says invalid session key, just ignore it. Now go back to the original wifi configuration page <a rel="nofollow" class="external free" href="http://192.168.1.254/wlswitchinterface0.wl">http://192.168.1.254/wlswitchinterface0.wl</a>:
</p><p><a href="http://subpos.org/wiki/index.php/File:Billion7800VDPXWifi.png" class="image"><img alt="Billion7800VDPXWifi.png" src="http://subpos.org/wiki/images/thumb/d/da/Billion7800VDPXWifi.png/400px-Billion7800VDPXWifi.png" decoding="async" width="400" height="350" srcset="/wiki/images/thumb/d/da/Billion7800VDPXWifi.png/600px-Billion7800VDPXWifi.png 1.5x, /wiki/images/thumb/d/da/Billion7800VDPXWifi.png/800px-Billion7800VDPXWifi.png 2x" /></a>
</p><p>You will see the new SSID. Make sure Enable is ticked and click Apply. Your new SubPos SSID will be available for positioning.
</p><p>Now you might want to restrict connections to that particular SSID, since enabling extra SSIDs on this router also creates an open network. To restrict all devices from connecting, go to the MAC Filter page for that interface:
</p><p><a href="http://subpos.org/wiki/index.php/File:MacFilter.png" class="image"><img alt="MacFilter.png" src="http://subpos.org/wiki/images/thumb/0/01/MacFilter.png/400px-MacFilter.png" decoding="async" width="400" height="310" srcset="/wiki/images/thumb/0/01/MacFilter.png/600px-MacFilter.png 1.5x, /wiki/images/thumb/0/01/MacFilter.png/800px-MacFilter.png 2x" /></a>
</p><p>Once at that page, select "Allow" for "MAC Restrict Mode". This will deny all unless you specify an allowed device in the list.
</p><p>SubPos is now enabled on your router!
</p>
