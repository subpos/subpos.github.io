<h2><span class="mw-headline" id="Personal_Identification_Information">Personal Identification Information</span></h2>
<p>We may collect personal identification information from Users in a variety of ways, including, but not limited to, when Users visit our site, register on the site, and in connection with other activities, services, features or resources we make available on our Site. Users may be asked for, as appropriate, name, email address. Users may, however, visit our Site anonymously. We will collect personal identification information from Users only if they voluntarily submit such information to us. Users can always refuse to supply personally identification information, except that it may prevent them from engaging in certain Site related activities.
</p>
<h2><span class="mw-headline" id="Non-personal_Identification_Information">Non-personal Identification Information</span></h2>
<p>We may collect non-personal identification information about Users whenever they interact with our Site. Non-personal identification information may include the browser name, the type of computer and technical information about Users means of connection to our Site, such as the operating system and the Internet service providers utilized and other similar information.
</p>
<h2><span class="mw-headline" id="Web_Browser_Cookies">Web Browser Cookies</span></h2>
<p>Our Site may use "cookies" to enhance User experience. User's web browser places cookies on their hard drive for record-keeping purposes and sometimes to track information about them. User may choose to set their web browser to refuse cookies, or to alert you when cookies are being sent. If they do so, note that some parts of the Site may not function properly.
</p>
<h2><span class="mw-headline" id="How_We_Use_Collected_Information">How We Use Collected Information</span></h2>
<p>SubPos may collect and use Users personal information for the following purposes:
</p>
<pre>- To run and operate our Site
</pre>
<p>We may need your information display content on the Site correctly.
</p>
<pre>- To personalize user experience
</pre>
<p>We may use information in the aggregate to understand how our Users as a group use the services and resources provided on our Site.
</p>
<pre>- To send periodic emails
</pre>
<p>We may use the email address to send User information and updates pertaining to their order. It may also be used to respond to their inquiries, questions, and/or other requests. 
</p>
<h2><span class="mw-headline" id="How_We_Protect_Your_Information">How We Protect Your Information</span></h2>
<p>We adopt appropriate data collection, storage and processing practices and security measures to protect against unauthorized access, alteration, disclosure or destruction of your personal information, username, password, transaction information and data stored on our Site.
</p>
<h2><span class="mw-headline" id="Sharing_Your_Personal_Information">Sharing Your Personal Information</span></h2>
<p>We do not sell, trade, or rent Users personal identification information to others.
</p>
<h2><span class="mw-headline" id="Electronic_Newsletters">Electronic Newsletters</span></h2>
<p>If User decides to opt-in to our mailing list, they will receive emails that may include company news, updates, related product or service information, etc.
</p>
<h2><span class="mw-headline" id="Third_Party_Websites">Third Party Websites</span></h2>
<p>Users may find advertising or other content on our Site that link to the sites and services of partners, suppliers, advertisers, sponsors, licencors and other third parties. We do not control the content or links that appear on these sites and are not responsible for the practices employed by websites linked to or from our Site. In addition, these sites or services, including their content and links, may be constantly changing. These sites and services may have their own privacy policies and customer service policies. Browsing and interaction on any other website, including websites which have a link to our Site, is subject to that website's own terms and policies.
</p>
<h2><span class="mw-headline" id="Changes_to_This_Privacy_Policy">Changes to This Privacy Policy</span></h2>
<p>SubPos has the discretion to update this privacy policy at any time. When we do, we will post a notification on the main page of our Site, revise the updated date at the bottom of this page and send you an email. We encourage Users to frequently check this page for any changes to stay informed about how we are helping to protect the personal information we collect. You acknowledge and agree that it is your responsibility to review this privacy policy periodically and become aware of modifications.
</p>
<h2><span class="mw-headline" id="Your_Acceptance_of_These_Terms">Your Acceptance of These Terms</span></h2>
<p>By using this Site, you signify your acceptance of this policy. If you do not agree to this policy, please do not use our Site. Your continued use of the Site following the posting of changes to this policy will be deemed your acceptance of those changes.
</p>
<h2><span class="mw-headline" id="Contacting_Us">Contacting Us</span></h2>
<p>If you have any questions about this Privacy Policy, the practices of this site, or your dealings with this site, please contact us via <a href="http://subpos.org/wiki/index.php/Special:Contact" class="new" title="Special:Contact (page does not exist)">this form</a>.
</p>
