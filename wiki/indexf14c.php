</p><textarea readonly="" accesskey="," id="wpTextbox1" cols="80" rows="25" style="" class="mw-editfont-monospace" lang="en" dir="ltr" name="wpTextbox1"> &lt;div class="center" style="width: auto; margin-left: auto; margin-right: auto;">''The SubPos Wi-Fi Positioning Standard is a "dataless" Wi-Fi positioning system that can be used anywhere GPS can't.''&lt;/div> 


The SubPos Wi-Fi Standard is an open source ([[SubPos:General_disclaimer|GPL]]), indoor positioning system that can be used in various environments such as metro lines, shopping malls, car parks, art galleries or conference centers, essentially anywhere GPS doesn't penetrate. SubPos is a complete positioning system that doesn't require expensive licencing, specialised hardware or laborious area profiling and isn't reliant on data connectivity (a connection to a database or cellphone coverage is not required).

The [[SubPos Standard|Standard]] defines a method for subterraneous positioning in different environments by exploiting all the capabilities of Wi-Fi. Plug and play SubPos Nodes or existing Wi-Fi access points are used to transmit encoded information in a standard Wi-Fi beacon frame which is then used for passive position calculation by a device of your choice (as long as it contains a Wi-Fi receiver). No more sending your position to external location providers when navigating indoors. Plus, since this is a purely client based system, [http://www.cisco.com/c/en/us/td/docs/solutions/Enterprise/Borderless_Networks/Unified_Access/CMX/CMX_LocFund.html#pgfId-1029530 location latency] is significantly reduced compared to a server-side implementation. It's a true indoor GPS!

Support SubPos by [[Access Point Augmentation|turning on positioning]] in your existing access point to help us create an open positioning system for all. 

