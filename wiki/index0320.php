== Personal Identification Information ==

We may collect personal identification information from Users in a variety of ways, including, but not limited to, when Users visit our site, register on the site, and in connection with other activities, services, features or resources we make available on our Site. Users may be asked for, as appropriate, name, email address. Users may, however, visit our Site anonymously. We will collect personal identification information from Users only if they voluntarily submit such information to us. Users can always refuse to supply personally identification information, except that it may prevent them from engaging in certain Site related activities.

== Non-personal Identification Information ==

We may collect non-personal identification information about Users whenever they interact with our Site. Non-personal identification information may include the browser name, the type of computer and technical information about Users means of connection to our Site, such as the operating system and the Internet service providers utilized and other similar information.

== Web Browser Cookies ==

Our Site may use "cookies" to enhance User experience. User's web browser places cookies on their hard drive for record-keeping purposes and sometimes to track information about them. User may choose to set their web browser to refuse cookies, or to alert you when cookies are being sent. If they do so, note that some parts of the Site may not function properly.

== How We Use Collected Information ==

SubPos may collect and use Users personal information for the following purposes:
 - To run and operate our Site
We may need your information display content on the Site correctly.
 - To personalize user experience
We may use information in the aggregate to understand how our Users as a group use the services and resources provided on our Site.
 - To send periodic emails
We may use the email address to send User information and updates pertaining to their order. It may also be used to respond to their inquiries, questions, and/or other requests. 

== How We Protect Your Information ==

We adopt appropriate data collection, storage and processing practices and security measures to protect against unauthorized access, alteration, disclosure or destruction of your personal information, username, password, transaction information and data stored on our Site.

== Sharing Your Personal Information ==

We do not sell, trade, or rent Users personal identification information to others.

== Electronic Newsletters ==

If User decides to opt-in to our mailing list, they will receive emails that may include company news, updates, related product or service information, etc.

== Third Party Websites ==

Users may find advertising or other content on our Site that link to the sites and services of partners, suppliers, advertisers, sponsors, licencors and other third parties. We do not control the content or links that appear on these sites and are not responsible for the practices employed by websites linked to or from our Site. In addition, these sites or services, including their content and links, may be constantly changing. These sites and services may have their own privacy policies and customer service policies. Browsing and interaction on any other website, including websites which have a link to our Site, is subject to that website's own terms and policies.

== Changes to This Privacy Policy ==

SubPos has the discretion to update this privacy policy at any time. When we do, we will post a notification on the main page of our Site, revise the updated date at the bottom of this page and send you an email. We encourage Users to frequently check this page for any changes to stay informed about how we are helping to protect the personal information we collect. You acknowledge and agree that it is your responsibility to review this privacy policy periodically and become aware of modifications.

== Your Acceptance of These Terms ==

By using this Site, you signify your acceptance of this policy. If you do not agree to this policy, please do not use our Site. Your continued use of the Site following the posting of changes to this policy will be deemed your acceptance of those changes.

== Contacting Us ==

If you have any questions about this Privacy Policy, the practices of this site, or your dealings with this site, please contact us via [[Special:Contact|this form]].
